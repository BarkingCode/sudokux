  // ============================================
  // PRD: Midnight reset works in user timezone
  // ============================================
  describe('Midnight Reset', () => {
    it('should use local device time for reset', () => {
      // PRD: "Puzzle resets at midnight local device time"
      // PRD: "Uses getLocalDateString() for date comparison"
      const getLocalDateString = (): string => {
        const now = new Date();
        return now.toISOString().split('T')[0];
      };

      const today = getLocalDateString();
      expect(today).toMatch(/^\d{4}-\d{2}-\d{2}$/);
    });

    it('should recognize date change at midnight', () => {
      // Use UTC timestamps to avoid timezone issues in tests
      const beforeMidnight = new Date('2024-01-15T23:59:59Z');
      const afterMidnight = new Date('2024-01-16T00:00:01Z');

      const beforeDate = beforeMidnight.toISOString().split('T')[0];
      const afterDate = afterMidnight.toISOString().split('T')[0];

      expect(beforeDate).toBe('2024-01-15');
      expect(afterDate).toBe('2024-01-16');
      expect(beforeDate).not.toBe(afterDate);
    });
  });

  // ============================================
