  // ============================================
  // PRD: Streak increments correctly
  // ============================================
  describe('Streak Rules', () => {
    const getLocalDateString = (date: Date): string => {
      return date.toISOString().split('T')[0];
    };

    const calculateStreak = (
      currentStreak: number,
      lastCompletedDate: string | null,
      today: string
    ): number => {
      if (!lastCompletedDate) {
        // First ever completion
        return 1;
      }

      const lastDate = new Date(lastCompletedDate);
      const todayDate = new Date(today);
      const yesterday = new Date(todayDate);
      yesterday.setDate(yesterday.getDate() - 1);

      const yesterdayStr = getLocalDateString(yesterday);

      if (lastCompletedDate === today) {
        // Already completed today - no change
        return currentStreak;
      } else if (lastCompletedDate === yesterdayStr) {
        // Last completed yesterday - increment
        return currentStreak + 1;
      } else {
        // Last completed 2+ days ago - reset to 1
        return 1;
      }
    };

    it('should increment streak when last completed was yesterday', () => {
      // PRD: "Last completed = yesterday | Increment streak"
      const currentStreak = 5;
      const today = '2024-01-15';
      const yesterday = '2024-01-14';

      const newStreak = calculateStreak(currentStreak, yesterday, today);
      expect(newStreak).toBe(6);
    });

    it('should keep streak when last completed was today (no double-count)', () => {
      // PRD: "Last completed = today | Keep current (no double-count)"
      const currentStreak = 5;
      const today = '2024-01-15';

      const newStreak = calculateStreak(currentStreak, today, today);
      expect(newStreak).toBe(5);
    });

    it('should reset streak to 1 when last completed was 2+ days ago', () => {
      // PRD: "Last completed = 2+ days ago | Reset to 1"
      const currentStreak = 10;
      const today = '2024-01-15';
      const twoDaysAgo = '2024-01-13';

      const newStreak = calculateStreak(currentStreak, twoDaysAgo, today);
      expect(newStreak).toBe(1);
    });

    it('should start at 1 for first ever completion', () => {
      // PRD: "First ever completion | Start at 1"
      const currentStreak = 0;
      const today = '2024-01-15';
      const lastCompleted = null;

      const newStreak = calculateStreak(currentStreak, lastCompleted, today);
      expect(newStreak).toBe(1);
    });

    it('should track best streak separately', () => {
      // PRD: "user_stats.best_daily_streak - all-time best"
      const currentStreak = 15;
      const bestStreak = 10;
      const newBestStreak = Math.max(currentStreak, bestStreak);
      expect(newBestStreak).toBe(15);
    });

    it('should trigger 7-day milestone achievement', () => {
      // PRD: "7-day streak - achievement + confetti"
      const streak = 7;
      const shouldTrigger7DayMilestone = streak === 7;
      expect(shouldTrigger7DayMilestone).toBe(true);
    });

    it('should trigger 30-day milestone achievement', () => {
      // PRD: "30-day streak - achievement + confetti"
      const streak = 30;
      const shouldTrigger30DayMilestone = streak === 30;
      expect(shouldTrigger30DayMilestone).toBe(true);
    });
  });

