    it('should not highlight user rank if beyond top 100', () => {
      const userRank = 150;
      const shouldHighlight = userRank <= 100;
      expect(shouldHighlight).toBe(false);
    });

    it('should rank by time_seconds ascending', () => {
      // PRD: "Rank by time_seconds ascending"
      const times = [120, 60, 180, 90];
      const sorted = [...times].sort((a, b) => a - b);
      expect(sorted).toEqual([60, 90, 120, 180]);
    });

    it('should break ties by completed_at (earlier wins)', () => {
      // PRD: "Ties broken by completed_at (earlier wins)"
      const entries = [
        { time_seconds: 60, completed_at: '2024-01-15T10:30:00Z' },
        { time_seconds: 60, completed_at: '2024-01-15T09:00:00Z' },
      ];

      const sorted = entries.sort((a, b) => {
        if (a.time_seconds !== b.time_seconds) {
          return a.time_seconds - b.time_seconds;
        }
        return (
          new Date(a.completed_at).getTime() -
          new Date(b.completed_at).getTime()
        );
      });

      expect(sorted[0].completed_at).toBe('2024-01-15T09:00:00Z');
    });
